//Cliclo while
/**
 * while se ejecuta siempre que la condicion sea verdadera
 while (condition) {
  
}
 */
var i=0;
while (i <= 10) {
  document.write(i+"<br>");
  i++;
}

var dias = ["lunes", "martes", "miercoles", "jueves", "viernes", "sabado", "domingo"];
var diasnum = dias.length -1;
var i=0;
while (i <= diasnum) {
  document.write(dias[i]+"<br>");
  i++;
}


